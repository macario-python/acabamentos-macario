# Texto (str)
nome_item = "Frango Grelhado"

# Número inteiro (int)
quantidade_vendida = 42

# Número decimal (float)
preco_unitario = 18.90

# Verdadeiro/Falso (bool)
item_disponivel = True

# Vendo o tipo de qualquer variável. Aqui é apenas pra vc saber qual é o tipo.
print(type(nome_item))        # <class 'str'>
print(type(quantidade_vendida))  # <class 'int'>
print(type(preco_unitario))   # <class 'float'>
print(type(item_disponivel))   # <class 'bool'>